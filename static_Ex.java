public class static_Ex {

    // Metodo estatico
    public static int suma(int a, int b) {
        int result; 
        result = a + b; 
        return result; 
    }

    // Variable estatica 
    public static void saludo() {
        System.out.println("Hola, soy una vaiable estatica, saludos!");
    }
}
